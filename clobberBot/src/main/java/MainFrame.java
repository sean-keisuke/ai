    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
    {//GEN-HEADEREND:event_jButton1ActionPerformed
        if (jFileChooser1.showOpenDialog(this) == JFileChooser.APPROVE_OPTION)
        {
            List<Bot> bots = Bots.load(jFileChooser1.getSelectedFile());
            for (Bot bot : bots)
            {
                botModel.addElement(bot);
            }
            JOptionPane.showMessageDialog(this, "Loaded " + bots.size() + " bots");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (jList1.isSelectionEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "You must select some bots", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Clobber game = Clobber.create(new String[0]);
        List<ClobberBot> bots = Bots.newInstance(game, jList1.getSelectedValues());
        new FreeForAllTourney(1000, game, bots, new RandomPlacement(), false).runTourney();
//        new FreeForAllTourney(1000, game, bots, new QueensAltPlacement(), false).runTourney();
        new FreeForAllTourney(1000, game, bots, new QueensPlacement(), false).runTourney();
        new PairTourney(100, game, bots, new RandomPlacement(), true).runTourney();
//        new PairTourney(1000, game, bots, new QueensAltPlacement(), true).runTourney();
        new PairTourney(100, game, bots, new QueensPlacement(), true).runTourney();
        new PairTourney(10, game, bots, new RandomPlacement(), false).runTourney();
//        new PairTourney(100, game, bots, new QueensAltPlacement(), false).runTourney();
        new PairTourney(10, game, bots, new QueensPlacement(), false).runTourney();
    }//GEN-LAST:event_jButton5ActionPerformed

