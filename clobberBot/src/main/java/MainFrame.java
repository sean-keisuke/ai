    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton4ActionPerformed
    {//GEN-HEADEREND:event_jButton4ActionPerformed
        if (jList1.isSelectionEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "You must select some bots", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Clobber game = Clobber.create(new String[0]);
        List<ClobberBot> bots = Bots.newInstance(game, jList1.getSelectedValues());
        MatchFrame matchFrame = new MatchFrame(game, bots);
        matchFrame.setVisible(true);
        matchFrame.start();
    }//GEN-LAST:event_jButton4ActionPerformed

